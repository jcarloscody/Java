
package cap6_metodos;


public class math_metodos {
    public static void main(String[] args) {
        System.out.println(Math.abs(23)); //valor absoluto
        System.out.println(Math.ceil(9.2));//arredonda para cima
        System.out.println(Math.floor(9.2));//arredonda para baixo
        System.out.println(Math.cos(0)); //cosseno trigonométrico
        System.out.println(Math.exp(2));//método exponencial
        System.out.println(Math.log(10));//logaritmo
        System.out.println(Math.max(2, 10));//maior valor de x  e y
        System.out.println(Math.min(2, 10));//menor valor de x e y
        System.out.println(Math.pow(12, 2));//x elevado À potência de y
        System.out.println(Math.sin(2)); //seno trign
        System.out.println(Math.sqrt(10));//raiz quadrada
        System.out.println(Math.tan(0));//tangente
        
        
        
        
    }
    
}
